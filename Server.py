import socket
from _thread import *
import pickle
from Game import Game

server = "127.0.0.1"
port = 5555

s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

try:
    s.bind((server, port))
except socket.error as e:
    str(e)

s.listen(2)
print("Waiting for a connection, Server Started")

games = {}
idCount = 0

"""
Function to handle a client connection in a separate thread.

Parameters:
- conn: The connection object representing the client connection.
- p: The player number associated with the client connection.
- gameId: The ID of the game associated with the client connection.

Returns:
None
"""
def threaded_client(conn, p, gameId):
    global idCount
    conn.send(str.encode(str(p)))

    reply = ""
    while True:
        try:
            data = conn.recv(4096).decode()

            if gameId in games:
                game = games[gameId]

                if not data:
                    break
                else:
                    if data == "reset":
                        game.resetWent()
                    elif data != "get":
                        game.play(p, data)

                    conn.sendall(pickle.dumps(game))
            else:
                break
        except:
            break

    conn.close()

    print("Lost connection")
    try:
        del games[gameId]
        print("Closing Game", gameId)
    except:
        pass
    idCount -= 1
    conn.close()


    """
    Shuts down the server by closing all client connections and the server socket.
    Does not take any parameters.
    Does not return any value.
    """
